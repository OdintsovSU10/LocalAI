export function tokenizeQuery(query) {
  return tokenize(query);
}

export function expandQueryTerms(terms) {
  const expanded = new Set(terms);
  for (const term of terms) {
    const group = synonymGroups.find((items) => items.includes(term));
    if (group) group.forEach((item) => expanded.add(item));
  }
  return Array.from(expanded);
}

export function prepareSearchQuery(query) {
  const originalTerms = tokenizeQuery(query);
  return {
    originalTerms,
    queryTerms: expandQueryTerms(originalTerms),
    phrase: String(query || "").trim().toLowerCase()
  };
}
